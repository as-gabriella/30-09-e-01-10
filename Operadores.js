//Operadores aritiméticos:
let soma = 10+5; // Soma
let subtração = 10 - 5; // Subtração
let multiplicação = 10 * 5; // Multiplicação
let divisão = 10 / 5;  // Divisão
let módulo = 10 % 3; // Resto da divsão (módulo)

console.log ("Soma",soma)
console.log ("Subtração",subtração)
console.log ("Multiplicação",multiplicação)
console.log ("Divisão",divisão)
console.log ("Módulo",módulo)

//Operadores de comparação:
//Maior que
console.log (10 > 5); //true

//Menor que
console.log (10 < 5); //false

//Menor OU Igual 
console.log ( 10 <= 70); //false

//Igual a
console.log ( 10 === 70); //false(igualdade)

//Operadores lógicos

console.log(true&&false); // false (E lógico)
console.log(true || false); // true (OU lógico) 
console.log (!true); //false (não lógico)